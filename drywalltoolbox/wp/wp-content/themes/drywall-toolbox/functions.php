<?php
/**
 * Drywall Toolbox — Active Theme functions.php
 *
 * This is the active WordPress theme for elliottm4.sg-host.com. Its sole job is
 * to serve the React SPA as the complete frontend. WordPress and WooCommerce
 * run exclusively as the headless CMS/REST API backend.
 *
 * Architecture:
 *  - WordPress installed at /wp/ subdirectory on the server.
 *  - React SPA build output lives at the site root in /dist/ (one level above
 *    ABSPATH), deployed by GitHub Actions on every push to main.
 *  - Asset manifest at /dist/asset-manifest.json maps logical chunk names to
 *    content-hashed filenames for cache busting.
 *  - WordPress enqueues the React build assets and outputs a window.DTB_CONFIG
 *    bootstrap object so React can read runtime values without hardcoding them.
 *
 * Responsibilities:
 *  1. Minimal theme setup.
 *  2. Enqueue React SPA assets from content-hashed asset manifest.
 *  3. Inject runtime config (window.DTB_CONFIG) into the page.
 *  4. Force all frontend requests to render via index.php (the React shell).
 *  5. Strip unnecessary WordPress head output.
 *
 * Cross-cutting security, CORS, Woo public read, nonce refresh, and admin
 * compatibility policy lives in mu-plugins so production behavior is theme-safe.
 *
 * @package drywall-toolbox
 * @version 2.0.0
 */

